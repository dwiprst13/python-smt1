#LATIHAN MENGETAHUI BENTUK DATA VARIABEL

#tipe data string
s = "Kamu Siapa"
print("Data :", s , "Data ini bertipe:", type(s))

#tipe data integer
i = 4
print("Data :", i , "Data ini bertipe:", type(i))

#tipe data float
f = 2.4
print("Data :", f , "Data ini bertipe:", type(f))

#tipe data boolean/biner
b = True
print("Data :", b , "Data ini bertipe:", type(b))

#tipe data tuple
t = 2,4
print("Data :", t , "Data ini bertipe:", type(t))

#tipe data complex
c = 2j
